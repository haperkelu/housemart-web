package org.housemart.service.enums;

public class OrgDescription {

	private int cross;
	private int above;
	private int own;
	public int getCross() {
		return cross;
	}
	public void setCross(int cross) {
		this.cross = cross;
	}
	public int getAbove() {
		return above;
	}
	public void setAbove(int above) {
		this.above = above;
	}
	public int getOwn() {
		return own;
	}
	public void setOwn(int own) {
		this.own = own;
	}
	
}
 